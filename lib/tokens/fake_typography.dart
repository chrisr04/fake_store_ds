/// A utility class that provides named constants for font family and text sizes.
class FakeTypography {
  FakeTypography._();

  /// The name of the primary font family used in the application.
  ///
  /// This font family is used for all text styles defined in this class.
  static const String poppins = 'Poppins';

  /// The font size for Heading 1 text.
  ///
  /// Typically used for the largest headings.
  static const double heading1 = 32.0;

  /// The font size for Heading 2 text.
  ///
  /// Typically used for second-level headings.
  static const double heading2 = 28.0;

  /// The font size for Heading 3 text.
  ///
  /// Typically used for third-level headings.
  static const double heading3 = 24.0;

  /// The font size for Heading 4 text.
  ///
  /// Typically used for fourth-level headings.
  static const double heading4 = 20.0;

  /// The font size for Heading 5 text.
  ///
  /// Typically used for fifth-level headings.
  static const double heading5 = 18.0;

  /// The font size for Heading 6 text.
  ///
  /// Typically used for sixth-level headings.
  static const double heading6 = 16.0;

  /// The font size for large text.
  ///
  /// Typically used for prominent text that is not a heading.
  static const double large = 14.0;

  /// The font size for medium text.
  ///
  /// Typically used for standard body text.
  static const double medium = 12.0;

  /// The font size for small text.
  ///
  /// Typically used for secondary text or captions.
  static const double small = 10.0;
}
